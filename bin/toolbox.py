"""Shared helpers for the Amp-Proc bin/ Python scripts.

Provides the standard logging helpers and the standardized log-file builder
described in the project CLAUDE.md. Because the bin/ scripts run standalone
(invoked by name from PATH, e.g. under Nextflow), import this module by adding
the script's own directory to sys.path first:

    import os, sys
    sys.path.insert(0, os.path.dirname(os.path.abspath(__file__)))
    from toolbox import log, log_warn, log_error, derive_sample_name, build_log

The log/log_warn/log_error helpers print to the console and also accumulate
ANSI-free messages that build_log() emits in the Info/Warnings/Errors sections
of the general-info block.
"""

import re
import sys
from datetime import datetime
from pathlib import Path

# ── Run state (per process) ──────────────────────────────────────────────────
_info = []
_warnings = []
_errors = []

_ANSI_RE = re.compile(r'\033\[[0-9;]*m')


def _clean(msg):
    """Strip ANSI color codes so the log file stays plain text."""
    return _ANSI_RE.sub('', str(msg))


def log(msg):
    """Log an informational message to stdout and the Info section."""
    print(f"[INFO] {msg}")
    _info.append(_clean(msg))


def log_warn(msg):
    """Log a warning to stderr and the Warnings section."""
    print(f"\033[1;33m[WARN]\033[0m {msg}", file=sys.stderr)
    _warnings.append(_clean(msg))


def log_error(msg):
    """Log an error to stderr and the Errors section."""
    print(f"\033[0;31m[ERROR]\033[0m {msg}", file=sys.stderr)
    _errors.append(_clean(msg))


def derive_sample_name(reads1, strip_read_suffix=False, sanitize=False):
    """Derive a sample name from an R1 filename.

    strip_read_suffix: drop a trailing _R1/-R1 (and anything after it).
    sanitize:          replace '-' with '_' (vsearch-safe sample labels).
    """
    name = Path(reads1).name
    for ext in (".fastq.gz", ".fq.gz", ".fastq", ".fq"):
        if name.endswith(ext):
            name = name[: -len(ext)]
            break
    if strip_read_suffix:
        stripped = re.sub(r'[_\-][Rr]1(?:[_\-].*)?$', '', name)
        name = stripped if stripped else name
    if sanitize:
        name = name.replace("-", "_")
    return name


def _section(title, items):
    if items:
        return [f"{title}:"] + [f"    {x}" for x in items]
    return [f"{title}:", "    None"]


def build_log(script_name, script_desc, sample_name, inputs, params,
              outputs, command, exit_status, tool_log=""):
    """Assemble the standardized log file following the project CLAUDE.md.

    The general-info block (script name, description, date, sample, inputs,
    parameters, outputs, command, captured Info/Warnings/Errors and exit
    status) must not contain any data statistics; those live in stats/.
    The third-party tool output, if any, is appended in its own section.
    """
    sep = "=" * 80
    lines = [
        sep, "Start of general log info", sep,
        "",
        f"Script name:",
        f"    {script_name}",
        "",
        f"Script description:",
        f"    {script_desc}",
        "",
        f"Date:",
        f"    {datetime.now()}",
        "",
        f"Sample name:",
        f"    {sample_name}",
        "",
        "Input data:",
    ]
    lines += [f"    {x}" for x in inputs] + [""]
    lines.append("Parameters:")
    lines += [f"    {x}" for x in params] + [""]
    lines.append("Output data:")
    lines += [f"    {x}" for x in outputs] + [""]
    lines += ["- Command executed:", f"    {command}", ""]
    lines += _section("Info", _info) + [""]
    lines += _section("Warnings", _warnings) + [""]
    lines += _section("Errors", _errors) + [""]
    lines += [f"Exit status: {exit_status}", "",
              sep, "End of general log info.", sep] + [""]
    if tool_log:
        lines += [sep, "Log info generated by third-party tools.", sep]
        lines.append(tool_log.rstrip("\n"))
    return "\n".join(lines) + "\n"
